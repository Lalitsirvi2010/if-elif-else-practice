
temp = float(input("Enter the temperature outside (in celsius)"))

if temp < 0 :
    print("Freezing weather")

elif temp < 10 :
    print("Very cold, wear a jacket")

elif temp < 25  : 
    print("Weather is nice") 

elif temp < 35:
    print("It's warm, drink water")    

else :
    print("Very hot, stay indoors")

